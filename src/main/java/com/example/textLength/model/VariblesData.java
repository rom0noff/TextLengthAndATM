package com.example.textLength.model;

public class VariblesData {
    private String text;

    public VariblesData() {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return "VariblesData{" +
                "text='" + text + '\'' +
                '}';
    }
}
